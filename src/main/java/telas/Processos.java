    public void PopularJTable() {
        
        ConexaoDAO conexao = new ConexaoDAO();
        Connection conn = conexao.conectaBD();

        if (conn != null) {
            String sql = "SELECT id_processo, 'Transferencia' AS servico, modelo_veiculo FROM transferencia UNION SELECT id_processo, 'Emplacamento' AS servico, modelo_veiculo FROM emplacamento";


            try {
                PreparedStatement pstm = conn.prepareStatement(sql);
                ResultSet resultado = pstm.executeQuery();

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setNumRows(0); 

                while (resultado.next()) {
                    model.addRow(new Object[]{
                        resultado.getInt("id_processo"),
                        resultado.getString("servico"),
                        resultado.getString("modelo_veiculo")
                    });
                }

                pstm.close();
                conn.close();

            } catch (SQLException ex) {
                Logger.getLogger(Processos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

